#!/usr/bin/env python3
"""
draw_rack.py — Rack-mount engineering drawing sheet.

Produces three output SVGs:
  sheet_front.svg      — Front elevation with BOM table + title block
  sheet_ortho.svg      — Orthographic sheet (front + side elevation)
  sheet_isometric.svg  — Isometric view with title block

The 3D view SVGs are generated by model.py (build123d / OCCT).
The BOM table and title block are overlaid via drawsvg.

Usage:
    python draw_rack.py
    python draw_rack.py --config rack.json --out-dir .
"""

import argparse
import json
import os
from pathlib import Path

import drawsvg as draw

from components import (
    bom_table,
    DIM_COLOUR,
    FONT,
    FS_LABEL,
    FS_HEAD,
)

# ── Sheet geometry ────────────────────────────────────────────────────────────

SHEET_W = 1200
SHEET_H = 850
MARGIN = 28
TITLE_H = 62


# ── SVG viewport embed ────────────────────────────────────────────────────────


def embed_svg_as_image(
    d: draw.Drawing,
    svg_path: str,
    x: float,
    y: float,
    w: float,
    h: float,
) -> None:
    """Embed an SVG file as a base64-encoded <image> element (self-contained output)."""
    import base64

    with open(svg_path, "rb") as f:
        data = base64.b64encode(f.read()).decode("ascii")
    uri = f"data:image/svg+xml;base64,{data}"
    d.append(draw.Image(x, y, w, h, uri, embed=False))


# ── Title block ───────────────────────────────────────────────────────────────


def draw_title_block(d: draw.Drawing, cfg: dict, view_name: str) -> None:
    tb_y = SHEET_H - TITLE_H - MARGIN
    tb_h = TITLE_H
    tb_w = SHEET_W - 2 * MARGIN

    d.append(
        draw.Rectangle(
            MARGIN, tb_y, tb_w, tb_h, fill="white", stroke="#333", stroke_width=1.0
        )
    )

    divs = [400, 200, 140]
    cx = MARGIN
    for dv in divs:
        cx += dv
        d.append(draw.Line(cx, tb_y, cx, tb_y + tb_h, stroke="#333", stroke_width=0.5))

    d.append(
        draw.Text(
            cfg.get("title", "Rack Mount Drawing"),
            FS_HEAD,
            MARGIN + 200,
            tb_y + 18,
            text_anchor="middle",
            dominant_baseline="middle",
            font_family=FONT,
            fill="#1a1a2e",
            font_weight="bold",
        )
    )
    d.append(
        draw.Text(
            f"View: {view_name}  —  velocity.report Sensor Mount",
            FS_LABEL,
            MARGIN + 200,
            tb_y + 40,
            text_anchor="middle",
            font_family=FONT,
            fill="#555",
        )
    )

    d.append(
        draw.Text(
            f"Rev {cfg.get('revision', 'A')}",
            FS_LABEL,
            MARGIN + 400 + 100,
            tb_y + 22,
            text_anchor="middle",
            dominant_baseline="middle",
            font_family=FONT,
            fill="#1a1a2e",
            font_weight="bold",
        )
    )

    notes = [
        "All dims: inches unless noted.",
        '2×4 nominal = 1.5"×3.5" actual.',
        "Qty 2 mounts required.",
    ]
    nx = MARGIN + 400 + 200 + 70
    for i, note in enumerate(notes):
        d.append(
            draw.Text(note, 7, nx, tb_y + 10 + i * 17, font_family=FONT, fill="#333")
        )

    d.append(
        draw.Text(
            "THIRD-ANGLE PROJECTION",
            FS_LABEL - 1,
            SHEET_W - MARGIN - 60,
            tb_y + 22,
            text_anchor="middle",
            font_family=FONT,
            fill="#555",
        )
    )
    d.append(
        draw.Text(
            "SCALE: 1:10 approx.",
            FS_LABEL - 1,
            SHEET_W - MARGIN - 60,
            tb_y + 40,
            text_anchor="middle",
            font_family=FONT,
            fill="#555",
        )
    )

    d.append(
        draw.Rectangle(
            MARGIN,
            MARGIN,
            SHEET_W - 2 * MARGIN,
            SHEET_H - 2 * MARGIN,
            fill="none",
            stroke="#333",
            stroke_width=1.5,
        )
    )


def view_label(d: draw.Drawing, text: str, x: float, y: float) -> None:
    d.append(
        draw.Text(
            text,
            FS_LABEL,
            x,
            y,
            text_anchor="middle",
            font_family=FONT,
            fill=DIM_COLOUR,
            font_weight="bold",
        )
    )


# ── Sheet builders ────────────────────────────────────────────────────────────


def build_front_sheet(cfg: dict, front_svg: str, out_path: str) -> None:
    d = draw.Drawing(SHEET_W, SHEET_H)
    d.append(draw.Rectangle(0, 0, SHEET_W, SHEET_H, fill="white"))

    vw, vh = 620, 460
    embed_svg_as_image(d, front_svg, MARGIN, MARGIN + 10, vw, vh)
    view_label(d, "FRONT ELEVATION", MARGIN + vw / 2, MARGIN + 10 + vh + 14)

    d.append(
        draw.Line(
            MARGIN + vw + 10,
            MARGIN,
            MARGIN + vw + 10,
            SHEET_H - TITLE_H - MARGIN - 10,
            stroke="#bbb",
            stroke_width=0.8,
        )
    )

    bom_table(
        d,
        x=MARGIN + vw + 22,
        y=MARGIN + 10,
        items=cfg["bom"],
        title="BILL OF MATERIALS  —  Sensor Mount Subassembly",
    )

    draw_title_block(d, cfg, "FRONT ELEVATION")
    d.save_svg(out_path)
    print(f"Saved: {out_path}")


def build_ortho_sheet(cfg: dict, front_svg: str, side_svg: str, out_path: str) -> None:
    d = draw.Drawing(SHEET_W, SHEET_H)
    d.append(draw.Rectangle(0, 0, SHEET_W, SHEET_H, fill="white"))

    half_w = (SHEET_W - 3 * MARGIN) // 2
    vh = 460

    embed_svg_as_image(d, front_svg, MARGIN, MARGIN + 10, half_w, vh)
    view_label(d, "FRONT ELEVATION", MARGIN + half_w / 2, MARGIN + 10 + vh + 14)

    embed_svg_as_image(d, side_svg, MARGIN * 2 + half_w, MARGIN + 10, half_w, vh)
    view_label(
        d, "SIDE ELEVATION", MARGIN * 2 + half_w + half_w / 2, MARGIN + 10 + vh + 14
    )

    d.append(
        draw.Line(
            int(MARGIN * 1.5 + half_w),
            MARGIN + 5,
            int(MARGIN * 1.5 + half_w),
            SHEET_H - TITLE_H - MARGIN - 5,
            stroke="#bbb",
            stroke_width=0.8,
        )
    )

    draw_title_block(d, cfg, "ORTHOGRAPHIC PROJECTION")
    d.save_svg(out_path)
    print(f"Saved: {out_path}")


def build_iso_sheet(cfg: dict, iso_svg: str, out_path: str) -> None:
    d = draw.Drawing(SHEET_W, SHEET_H)
    d.append(draw.Rectangle(0, 0, SHEET_W, SHEET_H, fill="white"))

    vw = SHEET_W - 2 * MARGIN
    vh = SHEET_H - TITLE_H - 3 * MARGIN

    embed_svg_as_image(d, iso_svg, MARGIN, MARGIN, vw, vh)
    view_label(d, "ISOMETRIC VIEW  —  Full Assembly", SHEET_W / 2, MARGIN + vh + 14)

    draw_title_block(d, cfg, "ISOMETRIC VIEW")
    d.save_svg(out_path)
    print(f"Saved: {out_path}")


# ── Entry point ───────────────────────────────────────────────────────────────


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Build rack-mount SVG sheet drawings from 3D model views"
    )
    parser.add_argument("--config", default="rack.json")
    parser.add_argument("--out-dir", default=None)
    args = parser.parse_args()

    script_dir = Path(__file__).parent
    out_dir = Path(args.out_dir) if args.out_dir else script_dir
    config_path = (
        Path(args.config) if os.path.isabs(args.config) else script_dir / args.config
    )

    with open(config_path) as f:
        cfg = json.load(f)

    # ── Step 1: generate 3D model views ──────────────────────────────────
    print("Building 3D model views…")
    from model import main as model_main  # noqa: PLC0415 (intentional late import)

    model_main(str(config_path), str(out_dir))

    front_3d = str(out_dir / "rack_front.svg")
    side_3d = str(out_dir / "rack_side.svg")
    iso_3d = str(out_dir / "rack_isometric.svg")

    # ── Step 2: assemble sheets ───────────────────────────────────────────
    print("Assembling drawing sheets…")
    build_front_sheet(cfg, front_3d, str(out_dir / "sheet_front.svg"))
    build_ortho_sheet(cfg, front_3d, side_3d, str(out_dir / "sheet_ortho.svg"))
    build_iso_sheet(cfg, iso_3d, str(out_dir / "sheet_isometric.svg"))

    print("Done.")


if __name__ == "__main__":
    main()
